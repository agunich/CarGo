package com.alexgunich.cargo.product.domain.vo;

/**
 * Represents the size of a product.
 * <p>
 * This enum defines the standard sizes available for a product.
 * The sizes include:
 * </p>
 * <ul>
 *   <li>XS - Extra Small</li>
 *   <li>S - Small</li>
 *   <li>M - Medium</li>
 *   <li>L - Large</li>
 *   <li>XL - Extra Large</li>
 *   <li>XXL - Double Extra Large</li>
 * </ul>
 */
public enum ProductSize {
  XS, S, M, L, XL, XXL
}
